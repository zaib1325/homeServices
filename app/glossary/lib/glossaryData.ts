export type PageType =
  | { type: "CATEGORY"; letter: string }
  | { type: "DEFINITION"; data: TermObject }
  | null;

export function getGlossaryPageType(slug: string): PageType {
  // Check if it's a single letter or "num"
  if (/^[a-zA-Z]$/.test(slug) || slug === "num") {
    return { type: "CATEGORY", letter: slug.toUpperCase() };
  }

  // Check if it's a term slug
  const term = GLOSSARY_TERMS.find((t) => t.slug === slug);
  if (term) {
    return { type: "DEFINITION", data: term };
  }

  return null;
}
